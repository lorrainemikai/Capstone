package com.adzumi.capstone.ui;

import android.app.SearchManager;
import android.content.Context;
import android.content.Intent;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.support.v7.widget.Toolbar;
import android.view.Menu;
import android.view.MenuInflater;
import android.view.MenuItem;
import android.view.View;
import android.widget.ImageView;
import android.widget.Toast;

import com.adzumi.capstone.ClientActivity;
import com.adzumi.capstone.R;
import com.google.firebase.auth.FirebaseAuth;

import butterknife.BindView;
import butterknife.ButterKnife;

public class HomeActivity extends AppCompatActivity implements View.OnClickListener {

    @BindView(R.id.my_toolbar) Toolbar myToolbar;
    @BindView(R.id.cleanImageView) ImageView mCleanImageView;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_home);
        ButterKnife.bind(this);

        setSupportActionBar(myToolbar);
        mCleanImageView.setOnClickListener(this);
    }

    @Override
    public void onClick(View view){
        if (view == mCleanImageView){
            Intent intent = new Intent(HomeActivity.this, HairIntroActivity.class);
            startActivity(intent);
        }
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        getMenuInflater().inflate(R.menu.options_menu, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        int id = item.getItemId();

        if (id == R.id.action_logout) {
            logout();
            return true;
        }

        return super.onOptionsItemSelected(item);
    }

    private void logout() {
//        FirebaseAuth.getInstance().signOut();
        Intent intent = new Intent(HomeActivity.this, ClientActivity.class);
        startActivity(intent);
    }
}
